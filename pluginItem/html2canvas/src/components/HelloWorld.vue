<script>
import html2canvas from 'html2canvas';
import { saveAs } from 'file-saver';

export default {
  name: 'HelloWorld',
  props: {
    msg: String
  },
  methods: {
    html2canvas() {
      const dom = document.getElementById('html-to-canvas')
      // 方法创造Blob对象，用以展示canvas上的图片；这个图片文件可以被缓存或保存到本地，由用户代理端自行决定。如不特别指明，图片的类型默认为 image/png，分辨率为96dpi。
      html2canvas(dom, {
        useCORS: true,
      }).then(canvas => {
          canvas.toBlob((blob) => {
            if (blob) {
              saveAs(blob, 'html2canvas.png')
            }
          })
        })
    }
  }
}
